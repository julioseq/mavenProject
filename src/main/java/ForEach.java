public class ForEach {
    public static void main(String[]args){
        String [] nombres={"Ruby","Andry","Elizabeth","Angelica","Efren","Ernesto","Magali","Julio"};

        for (String recorrer:nombres){ // descripcion estructura foreach: for(tipo de dato con el que voy a trabajar + nombre : declaro el arreglo)
            System.out.println(recorrer);
        }
    }
}
